from .tarot_cards import TarotCard, Suit, Element, PsychologicalMapping


def get_complete_deck() -> list[TarotCard]:
    """Returns all 78 cards in the RWS deck"""
    return COMPLETE_DECK


# Helper function to create cards more concisely
def create_card(num, name, suit, elem, up_kw, rev_kw, up_mean, rev_mean,
                shadow, soul, career, romance, wellness, family, growth, school,
                dbt=None, cbt=None, mrt=None, metaphor="", prompt="",
                astro_sign=None, astro_planet=None):
    """Factory function for creating cards with less boilerplate"""

    dbt = dbt or []
    cbt = cbt or []
    mrt = mrt or []

    return TarotCard(
        number=num, name=name, suit=suit, element=elem,
        upright_keywords=up_kw, reversed_keywords=rev_kw,
        upright_meaning=up_mean, reversed_meaning=rev_mean,
        shadow_work=shadow, soul_lesson=soul,
        career_interpretation=career,
        romance_interpretation=romance,
        wellness_interpretation=wellness,
        family_interpretation=family,
        self_growth_interpretation=growth,
        school_interpretation=school,
        psychology=PsychologicalMapping(
            dbt_skills=dbt, cbt_concepts=cbt, mrt_pillars=mrt,
            core_metaphor=metaphor, growth_prompt=prompt
        ),
        astro_sign=astro_sign, astro_planet=astro_planet
    )


