/**
 * Get ASCII card by name
 */
export function getAsciiCard(cardName, isReversed = false) {
  const key = cardName.toLowerCase().replace(/\s+/g, '_');

  let card = ASCII_CARDS[key] || ASCII_CARDS.card_back;

  if (isReversed) {
    // Flip the card upside down (reverse line order)
    const lines = card.split('\n');
    card = lines.reverse().join('\n');
  }

  return card;
}

/**
 * Render ASCII card in React Native Text component
 */
export function AsciiCardDisplay({ cardName, isReversed, style }) {
  const asciiArt = getAsciiCard(cardName, isReversed);

  return (
    <Text style={[styles.asciiCard, style]}>
      {asciiArt}
    </Text>
  );
}

