/**
 * Get ASCII card by index
 */
export function getAsciiCard(cardIndex, isReversed = false) {
  let cardArt = ASCII_CARDS[cardIndex];

  if (!cardArt) {
    // Fallback card
    cardArt = `
╔═══════════════════════╗
║   CARD #${cardIndex}           ║
║                       ║
║        ◯              ║
║                       ║
║                       ║
║                       ║
║                       ║
╚═══════════════════════╝`;
  }

  // If reversed, add indicator
  if (isReversed) {
    cardArt = cardArt + '\n     ⬇️ REVERSED ⬇️';
  }

  return cardArt;
}

